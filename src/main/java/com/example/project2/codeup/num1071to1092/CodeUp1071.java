package com.example.project2.codeup.num1071to1092;

import java.util.Scanner;

public class CodeUp1071 {
    // 1073 문제와 동일
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        while (true) {
            int num = sc.nextInt();
            if (num == 0) {
                break;
            }
            System.out.println(num);
        }
    }
}
