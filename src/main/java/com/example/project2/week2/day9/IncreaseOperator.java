package com.example.project2.week2.day9;

public class IncreaseOperator {
    public static void main(String[] args) {
        int i = 10;
        System.out.println(i++); // 결과값 : 10
        System.out.println(i); // 결과값 : 11

        int j = 10;
        System.out.println(++j); // 결과값 : 11
        System.out.println(j); // 결과값 : 11

        int x = 10;
        System.out.println(x--); // 결과값 : 10
        System.out.println(x); // 결과값 : 9

        int y = 10;
        System.out.println(--y); // 결과값 : 9
        System.out.println(y); // 결과값 : 9
    }
}
