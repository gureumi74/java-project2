package com.example.project2.week2.day9;

public class BitOperatorEx {
    public static void main(String[] args) {
        int val1 = 1;
        System.out.println(val1 << 1); // 2배 2
        System.out.println(val1 << 2); // 2배 4

        int val2 = 32;
        System.out.println(val2 >> 1); // /2 16
        System.out.println(val2 >> 2); // /2 8
    }
}
