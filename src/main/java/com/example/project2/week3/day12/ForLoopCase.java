package com.example.project2.week3.day12;

public class ForLoopCase {
    public static void main(String[] args) {
        for(int i = 0; 2 * i <= 18; i++) {
            System.out.printf("%d ", i);
        }
        System.out.println();
        for(int i = 0; i * i <= 50; i++) {
            System.out.printf("%d ", i);
        }
    }
}
